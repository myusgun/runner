# -*- coding: utf-8 -*-

FLAG_NOT_IN_PROGRESS = 0x01000000
FLAG_IN_PROGRESS     = 0x02000000

NONE         = FLAG_NOT_IN_PROGRESS | 0x00000001
DONE         = FLAG_NOT_IN_PROGRESS | 0x00000002
ERROR        = FLAG_NOT_IN_PROGRESS | 0x00000003
STOP         = FLAG_NOT_IN_PROGRESS | 0x00000004
DISCONNECTED = FLAG_NOT_IN_PROGRESS | 0x00000006

INITIATING   = FLAG_IN_PROGRESS | 0x00000001
INITIALIZING = FLAG_IN_PROGRESS | 0x00000002
READY        = FLAG_IN_PROGRESS | 0x00000003
RUNNING      = FLAG_IN_PROGRESS | 0x00000004
WAITING      = FLAG_IN_PROGRESS | 0x00000005
VIRTUALIZING = FLAG_IN_PROGRESS | 0x00000006
VIRTUALIZED  = FLAG_IN_PROGRESS | 0x00000007
CONTINUING   = FLAG_IN_PROGRESS | 0x00000008
BACKING_UP   = FLAG_IN_PROGRESS | 0x00000009

def asString(statement):
	strings = {
		NONE         : 'NONE',
		DONE         : 'DONE',
		ERROR        : 'ERROR',
		STOP         : 'STOPPED',
		DISCONNECTED : 'DISCONNECTED',

		INITIATING   : 'INITIATING',
		INITIALIZING : 'INITIALIZING',
		READY        : 'READY',
		RUNNING      : 'RUNNING',
		WAITING      : 'WAITING',
		VIRTUALIZING : 'VIRTUALIZING',
		VIRTUALIZED  : 'VIRTUALIZED',
		CONTINUING   : 'CONTINUING',
		BACKING_UP   : 'BACKING UP'
	}

	return strings.get(statement, 'unknown state')

def isInProgress(statement):
	if statement & FLAG_IN_PROGRESS == FLAG_IN_PROGRESS:
		return True
	else:
		return False

